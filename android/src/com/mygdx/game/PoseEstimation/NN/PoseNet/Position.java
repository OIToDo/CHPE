package com.mygdx.game.PoseEstimation.NN.PoseNet;

/**
 * The type Position.
 */
public class Position {
    /**
     * The X.
     */
    public int x = 0;
    /**
     * The Y.
     */
    public int y = 0;

    /**
     * Gets x.
     *
     * @return the x
     */
    public int getX() {
        return x;
    }

    /**
     * Sets x.
     *
     * @param x the x
     */
    public void setX(int x) {
        this.x = x;
    }

    /**
     * Gets y.
     *
     * @return the y
     */
    public int getY() {
        return y;
    }

    /**
     * Sets y.
     *
     * @param y the y
     */
    public void setY(int y) {
        this.y = y;
    }
}
